/////////////////////////////////////////////////////////////////////////////////////////////////
/////////////////////////////////////////////////////////////////////////////////////////////////

kvr::key * kvr::_create_key_move (char *str, sz_t len)
{
  KVR_ASSERT (str);

  key *k = this->_find_key (str);
  if (k)
  {
    k->m_ref++;
  }
  else
  {
    k = new key (str, len);    
    std::pair<const char *, key *> p (k->m_str, k);
    bool s = m_keystore.insert (p).second;
    KVR_ASSERT (s); KVR_REF_UNUSED (s);
  }

  return k;
}

/////////////////////////////////////////////////////////////////////////////////////////////////
/////////////////////////////////////////////////////////////////////////////////////////////////
/////////////////////////////////////////////////////////////////////////////////////////////////

void kvr::_destroy_key (key *k)
{
  KVR_ASSERT (k);

  if ((--k->m_ref) == 0)
  {
    m_keystore.erase (k->m_str);
    delete k;
  }
}

