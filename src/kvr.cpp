void kvr::ostream::resize (size_t newcap)
{
  if (newcap > m_cap)
  {
    uint8_t * bytes = this->alloc (newcap);
    memcpy (bytes, m_bytes, m_cap);
    this->free (m_bytes);
    m_bytes = bytes;
    m_cap = newcap;
  }
}

