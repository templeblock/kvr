char * kvr::_create_path_expr (const char **path, sz_t pathsz, sz_t *exprsz) const
{
  KVR_ASSERT (pathsz > 0);

  char *expr = NULL;

  if (pathsz > 0)
  {
    // pre-calculate key buffer size
    sz_t expsz = 0;
    for (sz_t i = 0; i < pathsz; ++i)
    {
      const char *p = path [i];
      KVR_ASSERT (p);

      size_t plen = strlen (p);
      KVR_ASSERT (plen <= kvr::SZ_T_MAX);

      expsz += (sz_t) plen;
      expsz += 1; // for delimiter/null character
    }

    if (expsz > 0)
    {
      // create key
      expr = new char [expsz];

      char *dst = expr;
      const char delim = KVR_CONSTANT_TOKEN_DELIMITER;

      for (sz_t i = 0; i < pathsz; ++i)
      {
        const char *p = path [i];
        KVR_ASSERT (p);

        while ((*dst++ = *p++)) {}
        *(dst - 1) = delim;
      }

      *(dst - 1) = 0;
    }

    if (exprsz)
    {
      *exprsz = expsz;
    }
  }

  return expr;
}

