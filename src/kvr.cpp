kvr::value * kvr::value::conv_string ()
{
  if (!is_string ())
  {
    this->_clear ();
    m_flags |= VALUE_FLAG_TYPE_STT_STRING;
  }

  return this;
}

/////////////////////////////////////////////////////////////////////////////////////////////////
/////////////////////////////////////////////////////////////////////////////////////////////////
/////////////////////////////////////////////////////////////////////////////////////////////////

kvr::value * kvr::value::conv_boolean ()
{
  if (!is_boolean ())
  {
    this->_clear ();
    m_flags |= VALUE_FLAG_TYPE_BOOLEAN;
  }

  return this;
}

/////////////////////////////////////////////////////////////////////////////////////////////////
/////////////////////////////////////////////////////////////////////////////////////////////////
/////////////////////////////////////////////////////////////////////////////////////////////////

kvr::value * kvr::value::conv_integer ()
{
  if (!is_integer ())
  {
    if (is_float ())
    {
      int64_t i = (int64_t) m_data.n.f;
      m_data.n.i = i;
    }
    else
    {
      this->_clear ();
    }

    m_flags |= VALUE_FLAG_TYPE_NUMBER_INTEGER;
  }

  return this;
}

/////////////////////////////////////////////////////////////////////////////////////////////////
/////////////////////////////////////////////////////////////////////////////////////////////////
/////////////////////////////////////////////////////////////////////////////////////////////////

kvr::value * kvr::value::conv_float ()
{
  if (!is_float ())
  {
    if (is_integer ())
    {
      double f = (double) m_data.n.i;;
      m_data.n.f = f;
    }
    else
    {
      this->_clear ();
    }

    m_flags |= VALUE_FLAG_TYPE_NUMBER_FLOAT;
  }

  return this;
}

/////////////////////////////////////////////////////////////////////////////////////////////////
/////////////////////////////////////////////////////////////////////////////////////////////////
/////////////////////////////////////////////////////////////////////////////////////////////////

kvr::value * kvr::value::conv_null ()
{
  if (!is_null ())
  {
    this->_clear ();
    m_flags |= VALUE_FLAG_TYPE_NULL;
  }

  return this;
}

