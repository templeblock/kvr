void kvr::value::_destruct ()
{
  if (is_map ())
  {
    cursor c = fcursor ();
    pair   p;
    while (c.get (&p))
    {
      m_ctx->_destroy_key (p.m_k);
      m_ctx->_destroy_value (VALUE_FLAG_PARENT_MAP, p.m_v);      
    }
    m_data.m.deinit ();
  }
  else if (is_array ())
  {
    sz_t c = this->length ();
    while (c)
    {
      this->pop ();
      c = this->length ();
    }
    m_data.a.deinit ();
  }
  else if (this->_is_string_dynamic ())
  {
    KVR_ASSERT (m_data.s.m_dyn.data);
    delete [] m_data.s.m_dyn.data;
  }
}

