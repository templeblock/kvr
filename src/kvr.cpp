/////////////////////////////////////////////////////////////////////////////////////////////////

kvr::~kvr ()
{
  KVR_ASSERT (m_keystore.empty ()); // debug warning assert

  // clean up left-over keys
  keystore::iterator iter = m_keystore.begin ();  
  while (iter != m_keystore.end ())
  {
    key *k = (*iter).second;
    KVR_ASSERT (k);
    delete k;

    m_keystore.erase (iter);
    iter = m_keystore.begin ();
  }

  //this->dump ();
}

