bool kvr::value::_insert_kv (key *k, value *v)
{
  KVR_ASSERT (k);
  KVR_ASSERT (v);
  KVR_ASSERT (is_map ());

  map::node *n = NULL;

#if KVR_DEBUG
  n = (k->m_ref <= 1) ? NULL : m_data.m.find (k);
  KVR_ASSERT (n == NULL);
#endif

  n = m_data.m.insert (k, v);
  return n ? true : false;
}

