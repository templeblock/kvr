{
  KVR_ASSERT (is_map () || is_array ());
  KVR_ASSERT_SAFE ((path && (pathsz > 0)), NULL);

  value *v = (value *) this;

  sz_t pc = 0;

  while (v && (pc < pathsz))
  {
    const char *key = path [pc++];
    v = v->_search_key (key);
  }

  return v;
}

