    pair *        insert (const char *key, const char *str);
    pair *        insert_map (const char *key);
    pair *        insert_array (const char *key);
    pair *        insert_null (const char *key);    
    bool          remove (pair *node);
    cursor        fcursor () const;

    // path search
    value *       search (const char *path) const;
    value *       search (const char **path, sz_t pathsz) const;

    // copy
    void          copy (const value *rhs);

    // debug log
    void          dump () const;
    
  private:

    ///////////////////////////////////////////
    ///////////////////////////////////////////
    ///////////////////////////////////////////

    union string
    {
      struct dyn_str
      {
        static const sz_t PAD = 8;

        char *  data;
        sz_t    size;
        sz_t    pad;
      } m_dyn;

      struct stt_str
      {
        static const sz_t CAP = ((sizeof (sz_t) * 2) + sizeof (char *));

        char data [CAP];
      } m_stt;
    };

    ///////////////////////////////////////////
    ///////////////////////////////////////////
    ///////////////////////////////////////////

    union number
    {
      int64_t   i;
      double    f;
    };

    ///////////////////////////////////////////
    ///////////////////////////////////////////
    ///////////////////////////////////////////

    struct array
    {
      static const sz_t CAP_INCR = 8;

      void    init (sz_t size);
      void    deinit ();
      void    push (value *v);
      value * pop ();

      value **m_ptr;
      sz_t    m_len;
      sz_t    m_cap;
    };

    ///////////////////////////////////////////
    ///////////////////////////////////////////
    ///////////////////////////////////////////

    struct map
    {
      static const sz_t CAP_INCR = 8;

      void    init (sz_t size);
      void    deinit ();
      pair *  insert (key *k, value *v);
      bool    remove (pair *p);
      pair *  find (const key *k) const;

      pair *  m_ptr;
      sz_t    m_size;
      sz_t    m_cap;     
    };

  public:

    ///////////////////////////////////////////
    ///////////////////////////////////////////
    ///////////////////////////////////////////

    class cursor
    {
    public:

      pair *  get_pair ();

    private:

      cursor (const map *m) : m_map (m), m_index (0) {}

      const map * m_map;
      sz_t        m_index;

      friend class value;
    };

  private:

    ///////////////////////////////////////////
    ///////////////////////////////////////////
    ///////////////////////////////////////////

    union data
    {
      number    n;
      map       m;
      array     a;
      string    s;
      bool      b;

      data () {}
    };

    ///////////////////////////////////////////
    ///////////////////////////////////////////
    ///////////////////////////////////////////

    void    _conv_map (sz_t size = map::CAP_INCR);
    void    _conv_array (sz_t size = array::CAP_INCR);

    void    _set_string_stt (const char *str);
    void    _set_string_dyn (const char *str, sz_t size);
    void    _move_string_dyn (char *str, sz_t size);

    pair  * _insert_kv (key *k, value *v);
    void    _push_v (value *v);
